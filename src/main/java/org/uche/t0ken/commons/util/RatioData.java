package org.uche.t0ken.commons.util;

import java.math.BigDecimal;

public class RatioData {
	private Float aligned;
	private Float opposed;
	private BigDecimal diff;
	private BigDecimal wt;
	private BigDecimal aDiff;
	private BigDecimal wt1;
	public Float getAligned() {
		return aligned;
	}
	public void setAligned(Float aligned) {
		this.aligned = aligned;
	}
	public Float getOpposed() {
		return opposed;
	}
	public void setOpposed(Float opposed) {
		this.opposed = opposed;
	}
	public BigDecimal getDiff() {
		return diff;
	}
	public void setDiff(BigDecimal diff) {
		this.diff = diff;
	}
	public BigDecimal getWt() {
		return wt;
	}
	public void setWt(BigDecimal wt) {
		this.wt = wt;
	}
	public BigDecimal getaDiff() {
		return aDiff;
	}
	public void setaDiff(BigDecimal aDiff) {
		this.aDiff = aDiff;
	}
	public BigDecimal getWt1() {
		return wt1;
	}
	public void setWt1(BigDecimal wt1) {
		this.wt1 = wt1;
	}
	
	
}
